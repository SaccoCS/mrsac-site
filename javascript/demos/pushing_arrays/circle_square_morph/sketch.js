function generateCircle() {
  var xs = [];
  var ys = [];
  for (var i = 0; i < 360; i +=5 ) {

    xs.push(200 + 150 * cos(i - 45));
    ys.push(200 + 150 * sin(i - 45));
  }

  circleXs = xs;
  circleYs = ys;
}

function generateSquare() {
  squareXs = [];
  squareYs = [];
  
  var space = 200/18

  for (var x = 100; x < 290; x += space) {
    squareYs.push(x);
    squareXs.push(300);
  }
  // console.log(squareXs.length);

  for (var x = 300; x >110; x -= space) {
     squareXs.push(x);
     squareYs.push(300);
  }
  // console.log(squareXs.length);
  
  for (var x = 300; x >110; x -= space) {
     squareXs.push(100);
     squareYs.push(x);
  }
  // console.log(squareXs.length);
  for (var x = 100; x < 290; x += space) {
    squareYs.push(100);
    squareXs.push(x);
  }
  // console.log(squareXs.length);
  
}

function draw() {

  background(bgCol);

  beginShape();
  for (var i = 0; i < xs.length; i++) {
    vertex(xs[i], ys[i]);
  }
  fill(col);
 // noFill()
 // noStroke();
  endShape(CLOSE);
  for (var i = 0; i < xs.length; i++) {

    fill(255);
    stroke(0);
    strokeWeight(3);



    if (pips.checked())
      ellipse(xs[i], ys[i], 9);
  }
  step();
  // jitter();
  if (frameCount % 50 == 0)
   flip();

}

function step() {
  for (var i = 0; i < xs.length; i++) {
    xs[i] = lerp(xs[i], dxs[i], .1);
    ys[i] = lerp(ys[i], dys[i], .1);
  }

  col = lerpColor(col, dCol, .1);
  bgCol = lerpColor(bgCol, bgDCol, .1);
}


